int ft_isalnum(int c)
{
    if((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') || (c >= '0' && c <= '9') )
    {
        return 1;
    }
    return 0;
}
//Bir karakterin alfanumerik(A-Z, a-z, 0-9) olup olmadığını kontrol eder.